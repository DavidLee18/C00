#include <unistd.h>

void	ft_putnbr(int i)
{
	char	c;

	if (i == -2147483648)
		write(1, "-2147483648", 11);
	else if (i < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-i);
	}
	else if (i < 10)
	{
		c = '0' + i;
		write(1, &c, 1);
	}
	else
	{
		c = '0' + i % 10;
		ft_putnbr(i / 10);
		write(1, &c, 1);
	}
}
